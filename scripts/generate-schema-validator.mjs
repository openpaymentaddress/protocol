import { readFile, writeFile } from "node:fs/promises";

import Ajv2020 from "ajv/dist/2020.js";
import standaloneCode from "ajv/dist/standalone/index.js";

const validators = [
  {
    schema: new URL(
      "../schema/open-payment-address-v1.schema.json",
      import.meta.url,
    ),
    output: new URL(
      "../packages/opap-core/src/generated/opap-schema-validator.ts",
      import.meta.url,
    ),
    name: "OPAP Record",
  },
];

const check = process.argv.includes("--check");
for (const definition of validators) {
  const schema = JSON.parse(await readFile(definition.schema, "utf8"));
  const ajv = new Ajv2020({
    allErrors: true,
    strict: true,
    code: { source: true, esm: true },
  });
  const validator = ajv.compile(schema);
  const output = `// Generated by scripts/generate-schema-validator.mjs. Do not edit.\n// @ts-nocheck\n${standaloneCode(ajv, validator)}`;

  if (check) {
    const current = await readFile(definition.output, "utf8").catch(() => "");
    if (current !== output) {
      throw new Error(
        `generated ${definition.name} schema validator is stale; run npm run schema:generate`,
      );
    }
    console.log(`Generated ${definition.name} schema validator is current`);
  } else {
    await writeFile(definition.output, output, "utf8");
    console.log(`Generated CSP-safe ${definition.name} schema validator`);
  }
}
